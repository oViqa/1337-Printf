#include "ft_printf.h"

void	putung(unsigned int n, int *i)
{
	if (n < 0)
	{
		ft_putchar('-', i);
		n = -n;
	}
	if (n >= 10)
	{
		putung(n / 10, i);
		putung(n % 10, i);
	}
	else
	{
		ft_putchar(n + '0', i);
	}
}
